__attribute__((always_inline)) static inline void mult64to128(unsigned long long int u, long long int v,
                                                              unsigned long long int* w_0, unsigned long long int* w_1)

{
   unsigned long long int u0, u1, v1;
   long long int v0, ts, ks;
   unsigned long long int k, t;
   unsigned long long int w1, w2, w3, w_0_temp;
   u0 = u >> 32;
   u1 = u & 0xFFFFFFFF;
   v0 = v >> 32;
   v1 = ((unsigned long long int)v) & 0xFFFFFFFF;
   t = u1 * v1;
   w3 = t & 0xFFFFFFFF;
   k = t >> 32;
   t = u0 * v1 + k;
   w2 = t & 0xFFFFFFFF;
   w1 = t >> 32;
   ts = u1 * v0 + w2;
   ks = ts >> 32;
   w_0_temp = u0 * v0 + w1 + ks;
   *w_0 = w_0_temp;
   *w_1 = (ts << 32) + w3;
}

