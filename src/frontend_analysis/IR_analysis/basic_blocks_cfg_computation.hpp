#ifndef BASIC_BLOCK_CFG_COMPUTATION_HPP
#define BASIC_BLOCK_CFG_COMPUTATION_HPP

/// Superclass include
#include "function_frontend_flow_step.hpp"

/**
 * Build call graph structures starting from the tree_manager.
 */
class BasicBlocksCfgComputation : public FunctionFrontendFlowStep
{
 private:
   /**
    * Return the set of analyses in relationship with this design step
    * @param relationship_type is the type of relationship to be considered
    */
   CustomUnorderedSet<std::pair<FrontendFlowStepType, FunctionRelationship>>
   ComputeFrontendRelationships(const DesignFlowStep::RelationshipType relationship_type) const override;

 public:
   /**
    * Constructor
    * @param Param is the set of the parameters
    * @param AppM is the application manager
    * @param fun_id is the function for which basic block control flow graph has to be built
    * @param design_flow_manager is the design flow manager
    */
   BasicBlocksCfgComputation(const ParameterConstRef _parameters, const application_managerRef AppM,
                             unsigned int _function_id, const DesignFlowManagerConstRef design_flow_manager);

   /**
    * Destructor
    */
   ~BasicBlocksCfgComputation() override;

   /**
    * Computes the basic block graph structure
    * @return the exit status of this step
    */
   DesignFlowStep_Status InternalExec() override;

   /**
    * Initialize the step (i.e., like a constructor, but executed just before exec
    */
   void Initialize() override;
};
#endif
