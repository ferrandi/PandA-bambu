#include "panda_types.hpp"

unsigned long long panda::integer_cst_bitsize(const integer_cst_t& ic)
{
#ifdef UNLIMITED_PRECISION
   return ic.minBitwidth(ic < 0);
#else
   if(ic == 0 || ic == -1)
   {
      return 1ull;
   }
   else if(ic < -1)
   {
      return 65ull - static_cast<unsigned long long>(__builtin_clzll(static_cast<unsigned long long>(~ic)));
   }
   return 64ull - static_cast<unsigned long long>(__builtin_clzll(static_cast<unsigned long long>(ic)));
#endif
}
